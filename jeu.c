#include <stdlib.h>
#include "jeu.h"



void vider_historique(PILE* historique)
{
	PILE tmp;
	tmp = *historique;
	
	while(tmp!=NULL)
	{
		tmp = (*historique)->suivant;
		free(*historique);
		*historique = tmp;
	}


}







